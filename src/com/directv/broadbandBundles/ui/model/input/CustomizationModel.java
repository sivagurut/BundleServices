public class CustomizationModel
{
    private long visitId;
    private boolean creditCardRequiredFlag;
    private String punchOutURL;
    private CustomizationGroupList groupList;
    private boolean customizationCompletedFlag;
    private boolean immediateSubmissionFlag;
    private String errorMessage;


    public String getErrorMessage()
    {
        return errorMessage;
    }

    public void setErrorMessage(String errorMessage)
    {
        this.errorMessage = errorMessage;
    }

    public boolean isImmediateSubmissionFlag()
    {
        return immediateSubmissionFlag;
    }

    public void setImmediateSubmissionFlag(boolean immediateSubmissionFlag)
    {
        this.immediateSubmissionFlag = immediateSubmissionFlag;
    }

    public long getVisitId()
    {
        return visitId;
    }

    public void setVisitId(long visitId)
    {
        this.visitId = visitId;
    }

    public boolean isCreditCardRequiredFlag()
    {
        return creditCardRequiredFlag;
    }

    public void setCreditCardRequiredFlag(boolean creditCardRequiredFlag)
    {
        this.creditCardRequiredFlag = creditCardRequiredFlag;
    }

    public String getPunchOutURL()
    {
        return punchOutURL;
    }

    public void setPunchOutURL(String punchOutURL)
    {
        this.punchOutURL = punchOutURL;
    }

    public CustomizationGroupList getGroupList()
    {
        return groupList;
    }

    public void setGroupList(CustomizationGroupList groupList)
    {
        this.groupList = groupList;
    }

    public boolean isCustomizationCompletedFlag()
    {
        return customizationCompletedFlag;
    }

    public void setCustomizationCompletedFlag(boolean customizationCompletedFlag)
    {
        this.customizationCompletedFlag = customizationCompletedFlag;
    }

    @Override
    public String toString()
    {
        return "CustomizationModel{" +
                "visitId=" + visitId +
                ", creditCardRequiredFlag=" + creditCardRequiredFlag +
                ", punchOutURL='" + punchOutURL + '\'' +
                ", groupList=" + groupList +
                ", customizationCompletedFlag=" + customizationCompletedFlag +
                ", immediateSubmissionFlag=" + immediateSubmissionFlag +
                ", errorMessage='" + errorMessage + '\'' +
                '}';
    }
}
